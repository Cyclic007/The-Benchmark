int checkingPrimes(int n,std::vector<int> primes,int maxPrime) {
    if (maxPrime<2) {
        return primes.size();
    }else {
        for(int i=2; i<n; i++) {
            if(n%i==0) {
                primes.push_back(i);
                return checkingPrimes(n+1,primes,maxPrime);
            }
        }
    }
}
